package com.kte.mvc.vo;

public class V5_Student {

	private String std_id = null;
	private String std_name = null;
	private String std_tel = null;
	private String std_date = null;
	
	public String getStd_id() {
		return std_id;
	}
	public void setStd_id(String std_id) {
		this.std_id = std_id;
	}
	public String getStd_name() {
		return std_name;
	}
	public void setStd_name(String std_name) {
		this.std_name = std_name;
	}
	public String getStd_tel() {
		return std_tel;
	}
	public void setStd_tel(String std_tel) {
		this.std_tel = std_tel;
	}
	public String getStd_date() {
		return std_date;
	}
	public void setStd_date(String std_date) {
		this.std_date = std_date;
	}
	
}
