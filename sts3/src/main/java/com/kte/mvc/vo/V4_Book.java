package com.kte.mvc.vo;

public class V4_Book {
	private String book_code = null;
	private String book_title = null;
	private String book_date = null;
	private String bookcategory_code = null;
	
	
	public String getBook_code() {
		return book_code;
	}
	public void setBook_code(String book_code) {
		this.book_code = book_code;
	}
	public String getBook_title() {
		return book_title;
	}
	public void setBook_title(String book_title) {
		this.book_title = book_title;
	}
	public String getBook_date() {
		return book_date;
	}
	public void setBook_date(String book_date) {
		this.book_date = book_date;
	}
	public String getBookcategory_code() {
		return bookcategory_code;
	}
	public void setBookcategory_code(String bookcategory_code) {
		this.bookcategory_code = bookcategory_code;
	}
	
	
}
